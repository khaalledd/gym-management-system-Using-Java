    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
       String id=lb_id.getText();
       String name=txt_name.getText();
       String mobilenumber=txt_mobile_number.getText();
       String email=txt_email.getText();
       String gender=(String)cb_gender.getSelectedItem();
       String fathername=txt_father_name.getText();
       String mothername=txt_mother_name.getText();
       String gymtime=(String)cb_gymtime.getSelectedItem();
       String city=txt_city.getText();
       String age=txt_age.getText();
       String amount=txt_amount.getText();
       try
       {
           Connection c=Connectinprovider.connect();
           PreparedStatement p=c.prepareStatement("insert into member values (?,?,?,?,?,?,?,?,?,?,?)");
           p.setString(1, id);
           p.setString(2, name);
           p.setString(3, mobilenumber);
           p.setString(4, email);
           p.setString(5, gender);
           p.setString(6, fathername);
           p.setString(7, mothername);
           p.setString(8, gymtime);
           p.setString(9, city);
           p.setString(10, age);
           p.setString(11, amount);
           p.executeUpdate();
           JOptionPane.showMessageDialog(null, "successfully saved");
           setVisible(false);
           new Newmember().setVisible(true);
       }
       catch(Exception ex)
       {
           JOptionPane.showMessageDialog(null, ex);
       }
       
    }//GEN-LAST:event_SaveActionPerformed

