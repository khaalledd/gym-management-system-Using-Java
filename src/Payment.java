    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
       tabledetails();
       int checkid=0;
       String id=txt_id.getText();
       String month=lb_date.getText();
       try
       {
           Connection c=Connectinprovider.connect();
           Statement s=c.createStatement();
           ResultSet r=s.executeQuery("select *from member where id='"+id+"'");
           while (r.next())
           {
               checkid=1;
               txt_id.setEditable(false);
               txt_name.setText(r.getString(2));
               txt_mobile_number.setText(r.getString(3));
               txt__email.setText(r.getString(4));
               txt_amount_of_pay.setText(r.getString(11));
           }
           if(checkid==0)
           
               JOptionPane.showMessageDialog(null, "Member ID doesn't exist");
           
           
           ResultSet rs=s.executeQuery("select *from payment inner join member where payment.month='"+month+"'and payment.id='"+id+"'and member.id='"+id+"'"); 
           while(rs.next())
           {
               btn_save.setVisible(false);
               JOptionPane.showMessageDialog(null, "payment is already done for this month");
           }
       }
       catch(Exception ex)
       {
           JOptionPane.showMessageDialog(null, ex);
       }   
       
    }//GEN-LAST:event_SearchActionPerformed

