    public void tabledetails()
    {
        DefaultTableModel dtm=(DefaultTableModel)tb_details.getModel();
        dtm.setRowCount(0);
        String id=txt_id.getText(); 
        try
        {
            Connection c=Connectinprovider.connect();
            Statement s=c.createStatement();
            ResultSet r=s.executeQuery("select *from payment where id ='"+id+"'");
            while (r.next())
            {
                dtm.addRow(new Object[]{r.getString(2),r.getString(3)});
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
